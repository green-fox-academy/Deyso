public class twoNumbers {

    public static void main(String[] args) {

        int firstNumber = 13;
        int secoundNumber = 22;
        double firstNumberDec = firstNumber;
        double secoundNumberDec = secoundNumber;

        System.out.println("first | " + (firstNumber + secoundNumber));
        System.out.println("substracted |  " + (firstNumber - secoundNumber));
        System.out.println("multiplied |  " + (firstNumber * secoundNumber));
        System.out.println("divided |  " + (secoundNumberDec / firstNumberDec));
        System.out.println("integer |  " + (secoundNumber / firstNumber));
        System.out.println("remainder |  " + (secoundNumber % firstNumber));


    }

}
